// Exercício de programação: contagem regressiva de 10 até 0.
// Utilização de parênteses para definir condição de loop.
// Inicialização da variável contagem com valor 10.
// Implementação de um loop while com a condição contagem >= 0.
// Impressão do valor de contagem em cada iteração.
// Atualização de contagem com decremento.

let n = 10

while (0 < n){
    console.log(n)
    n--
}

console.log(0)
